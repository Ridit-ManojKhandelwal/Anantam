const createWindow = (): void => {
  // Create the browser window.
  mainWindow = new BrowserWindow({
    height: 600,
    width: 800,
    icon: path.join(__dirname, "assets", "logo.png"),
    webPreferences: {
      preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
      nodeIntegration: true,
    },
  });

  mainWindow.maximize();

  // @ts-ignore
  const cwd = store.get(SELECTED_FOLDER_STORE_NAME).root;

  // Watch the directory for changes
  const watcher = chokidar.watch(cwd, { ignoreInitial: true });

  watcher
    .on("add", (path) => {
      // Get the folder structure of the root directory after adding a new file
      const tree = get_files(cwd); // Assuming cwd is the root directory being watched
      const structure = {
        name: path,
        root: cwd, // The root directory
        tree, // The updated folder structure
      };

      // @ts-ignore

      store.set(SELECTED_FOLDER_STORE_NAME, structure);

      // Notify the main window that a new folder has been opened
      mainWindow.webContents.send("new-folder-opened");
    })
    .on("addDir", (path) => {
      console.log(`Directory added: ${path}`);

      // Get the folder structure of the root directory after adding a directory
      const tree = get_files(cwd);
      const structure = {
        name: path,
        root: cwd, // The root directory
        tree, // The updated folder structure
      };

      // @ts-ignore

      store.set(SELECTED_FOLDER_STORE_NAME, structure);
      mainWindow.webContents.send("new-folder-opened");
    })
    .on("unlink", (path) => {
      console.log(`File removed: ${path}`);

      // Get the folder structure of the root directory after removing a file
      const tree = get_files(cwd);
      const structure = {
        name: path,
        root: cwd, // The root directory
        tree, // The updated folder structure
      };

      // @ts-ignore

      store.set(SELECTED_FOLDER_STORE_NAME, structure);
      mainWindow.webContents.send("folder-updated");
    })
    .on("unlinkDir", (path) => {
      console.log(`Directory removed: ${path}`);

      // Get the folder structure of the root directory after removing a directory
      const tree = get_files(cwd);
      const structure = {
        name: path,
        root: cwd, // The root directory
        tree, // The updated folder structure
      };

      // @ts-ignore

      store.set(SELECTED_FOLDER_STORE_NAME, structure);
      mainWindow.webContents.send("folder-updated");
    })
    .on("rename", (oldPath, newPath) => {
      console.log(`File/Directory renamed from ${oldPath} to ${newPath}`);

      // Get the folder structure of the root directory after renaming
      const tree = get_files(cwd);
      const structure = {
        name: newPath,
        root: cwd, // The root directory
        tree, // The updated folder structure
      };

      // @ts-ignore

      store.set(SELECTED_FOLDER_STORE_NAME, structure);
      mainWindow.webContents.send("file-renamed");
    })
    .on("error", (error) => {
      console.error(`Watcher error: ${error}`);
    });

  // and load the index.html of the app.
  mainWindow.loadURL(MAIN_WINDOW_WEBPACK_ENTRY);

  const shell = os.platform() === "win32" ? "powershell.exe" : "bash";

  ptyProcess = pty.spawn(shell, [], {
    name: "xterm-color",
    cols: 80,
    rows: 30,
    cwd: cwd || process.env.HOME,
    env: process.env,
  });

  ptyProcess.onData(function (data) {
    mainWindow.webContents.send("terminal.incomingData", data);
    console.log("Data sent");
  });

  ipcMain.on("terminal.keystroke", (event, key) => {
    ptyProcess.write(key);
  });

  ipcMain.on("terminal.resize", (event, data) => {
    ptyProcess.resize(data.cols, data.rows);
  });

  ipcMain.on("terminal.write", (event, line) => {
    ptyProcess.write(line);
  });

  ipcMain.handle("open-folder", async (event, data) => {
    const folder = await dialog.showOpenDialog(mainWindow, {
      properties: ["openDirectory"],
    });
    let structure = undefined;
    if (!folder.canceled) {
      const tree = get_files(folder.filePaths[0]);
      structure = {
        // name: path.dirname(folder.filePaths[0]),
        name: folder.filePaths[0],
        root: folder.filePaths[0],
        tree,
      };
      // @ts-ignore
      store.set(SELECTED_FOLDER_STORE_NAME, structure);
    }

    return structure;
  });

  // Register the ipcMain handler for 'dialog:openFolder'
  ipcMain.handle("open-folder-new-project", async (event, data) => {
    const folder = await dialog.showOpenDialog(mainWindow, {
      properties: ["openDirectory"],
    });

    return folder.filePaths[0]; // Return the selected folder pathr;
  });

  mainWindow.once("ready-to-show", () => {
    mainWindow.show(); // Show the main window
  });

  // Open the DevTools.
  mainWindow.webContents.openDevTools();
};

