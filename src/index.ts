ipcMain.handle("run-code", async (event, data) => {
  if (!data.path.endsWith(".py")) {
    if (data.path.endsWith(".js")) {
      ptyProcess.write(`node ${data.path}\n`);
    }
    return;
  }

  const command = `python ${data.path}`; // Command used to run Python script
  event.sender.send("show-code", {
    output: "Running code...",
    command: command,
    exited: 0,
    isError: false,
    error: "",
  });

  const options = {
    scriptPath: "",
    pythonOptions: ["-u"],
  };

  const pythonShell = new PythonShell(data.path, options);

  pythonShell.on("message", (message) => {
    event.sender.send("show-code", {
      output: message,
      command: command,
      exited: 0, // assuming no error occurred
      isError: false,
      error: "",
    });
  });

  pythonShell.on("stderr", (stderrMessage) => {
    event.sender.send("show-code", {
      output: ``,
      command: command,
      exited: 1, // indicates an error occurred
      isError: true,
      error: stderrMessage,
    });
  });

  pythonShell.on("error", (err) => {
    event.sender.send("show-code", {
      output: ``,
      command: command,
      exited: 1, // indicates an error occurred
      isError: true,
      error: err.message,
    });
  });

  pythonShell.end((err) => {
    if (err) {
      event.sender.send("show-code", {
        output: ``,
        command: command,
        exited: 1, // indicates an error occurred
        isError: true,
        error: err.message,
      });
    }
  });
});

